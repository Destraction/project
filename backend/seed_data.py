async def seed():
    async with AsyncSessionLocal() as db:
        # Сид-данные по концепции безалкогольного бара (200 уникальных ингредиентов).
        ingredients_def = _build_ingredients()

        names = [d["name"] for d in ingredients_def]
        existing_names = set(
            (await db.execute(select(Ingredient.name).where(Ingredient.name.in_(names)))).scalars().all()
        )

        to_add = [
            Ingredient(**d)
            for d in ingredients_def
            if d["name"] not in existing_names
        ]
        if to_add:
            db.add_all(to_add)
            await db.commit()

        # Получаем ID добавленных ингредиентов
        result = await db.execute(text("SELECT id, name FROM ingredients"))
        rows = result.fetchall()
        ing_by_name = {row[1]: row[0] for row in rows}

        # Добавляем базовые правила совместимости (score: 2 – хорошо, 1 – нейтрально)
        existing_pairs_rows = (await db.execute(select(Compatibility.ing1_id, Compatibility.ing2_id))).all()
        existing_pairs = {(row[0], row[1]) for row in existing_pairs_rows}

        compat_rules = [
            ("лимон", "мята", 2),
            ("лайм", "мята", 2),
            ("лимон", "сахарный сироп", 2),
            ("клубничный сироп", "лимон", 2),
            ("клубничный сироп", "лайм", 2),
            ("имбирь", "лимон", 2),
            ("имбирь", "мята", 1),
            ("апельсин", "корица", 2),
            ("яблоко", "корица", 2),
            ("груша", "ваниль", 2),
            ("ананас", "кокосовый сироп", 2),
            ("манго", "имбирь", 2),
            ("маракуйя", "лайм", 2),
            ("арбуз", "мята", 2),
            ("дыня", "мята", 2),
            ("клубника", "базилик", 2),
            ("малина", "розмарин", 1),
            ("грейпфрут", "розмарин", 2),
            ("киви", "яблочный сок", 2),
            ("гранат", "лайм", 2),
            ("облепиха", "медовый сироп", 2),
            ("вишня", "миндальный сироп", 2),
            ("черника", "лавандовый сироп", 1),
            ("апельсиновый сок", "гранатовый сироп", 1),
        ]

        to_add_compat = []
        for ing1_name, ing2_name, score in compat_rules:
            if ing1_name not in ing_by_name or ing2_name not in ing_by_name:
                continue
            ing1 = ing_by_name[ing1_name]
            ing2 = ing_by_name[ing2_name]
            if (ing1, ing2) not in existing_pairs:
                to_add_compat.append(Compatibility(ing1_id=ing1, ing2_id=ing2, score=score))

        if to_add_compat:
            db.add_all(to_add_compat)
            await db.commit()
        print("✅ Тестовые данные добавлены/обновлены (200 ингредиентов).")

if __name__ == "__main__":
    asyncio.run(seed())